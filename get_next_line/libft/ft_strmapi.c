#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int i, char))
{
	char			*d;
	unsigned char	i;

	i = 0;
	if (!s)
		return (NULL);
	d = ft_strnew(ft_strlen(s));
	if (!d)
		return (NULL);
	while (s[i])
	{
		d[i] = f(i, s[i]);
		i++;
	}
	d[i] = '\0';
	return (d);
}
